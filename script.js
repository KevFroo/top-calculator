// return the sum of two numbers
function add(n1, n2) {
    return n1 + n2;
}

// return the difference
function subtract(n1, n2) {
    return n1 - n2;
}

// return the product of two numbers
function multiply(n1, n2) {
    return n1 * n2;
}

// return the fraction of two numbers
function divide(n1, n2) {
    return n1 / n2;
}